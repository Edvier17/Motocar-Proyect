    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
String doc = txtDocumento.getText();
String placa = txtPlaca.getText();

Cliente c = ClienteControlador.buscarPorDocumento(doc);
Vehiculo v = VehiculoControlador.buscarPorPlaca(placa);

if (c != null && v != null) {
    Alquiler nuevo = new Alquiler(c, v);
    AlquilerControlador.registrarAlquiler(nuevo);

    JOptionPane.showMessageDialog(this, "Alquiler registrado correctamente.");
    txtDocumento.setText("");
    txtPlaca.setText("");
} else {
    JOptionPane.showMessageDialog(this, "Cliente o veh√≠culo no encontrado.");
}
        // TODO add your handling code here:
    }//GEN-LAST:event_btnRegistrarActionPerformed

